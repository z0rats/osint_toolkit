@router.get(
    "/{alert_id}",
    response_model=AlertSchema,
    response_model_exclude_none=True,
    summary="Get alert",
    description="Retrieve a specific alert by its ID",
    responses={404: {"description": "Alert not found"}},
)
async def get_alert(alert_id: AlertId, db: ReadSessionDep) -> AlertSchema:
    """Get a specific alert by ID"""
    alert = await alerts_crud.get_alert_by_id(db, alert_id)
    if not alert:
        raise AppHTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Alert not found",
            error_code="ALERT_NOT_FOUND",
        )
    return alert


@router.patch(
    "/{alert_id}",
    response_model=AlertSchema,
    response_model_exclude_none=True,
    summary="Update alert",
    description="Partially update an alert, for example to mark it as read or unread",
    responses={404: {"description": "Alert not found"}},
)
async def update_alert(alert_id: AlertId, update_data: AlertUpdateSchema, db: SessionDep) -> AlertSchema:
    """Partially update an alert (e.g. mark as read or unread)"""
    alert = await alerts_crud.update_alert_read_status(db, alert_id, update_data.read)
    if not alert:
        raise AppHTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Alert not found",
            error_code="ALERT_NOT_FOUND",
        )
    logger.info("Set alert %s read status to %s", alert_id, update_data.read)
    return alert


@router.delete(
    "/{alert_id}",
    status_code=status.HTTP_204_NO_CONTENT,
    summary="Delete alert",
    description="Permanently delete a specific alert by its ID",
    responses={404: {"description": "Alert not found"}},
)
async def delete_single_alert(alert_id: AlertId, db: SessionDep) -> None:
    """Delete a specific alert"""
    alert = await alerts_crud.delete_alert_by_id(db, alert_id)
    if not alert:
        raise AppHTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Alert not found",
            error_code="ALERT_NOT_FOUND",
        )
    logger.info("Deleted alert %s", alert_id)
