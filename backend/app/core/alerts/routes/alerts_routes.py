@router.get(
    "/count/unread",
    response_model=UnreadCountResponse,
    summary="Get unread alert count",
    description="Returns the number of alerts that have not been marked as read",
)
async def get_unread_count(db: ReadSessionDep) -> UnreadCountResponse:
    """Get count of unread alerts"""
    count = await alerts_crud.count_unread_alerts(db)
    return UnreadCountResponse(unread_count=count)


